    private void onayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onayBtnActionPerformed
        File f = new File(tarihText.getText());

        try {

            BufferedReader reader = new BufferedReader(new InputStreamReader(new FileInputStream(f)));
            int devam = 1;
            while (devam != 0) {
                String satir = reader.readLine();
                switch (satir) {
                    case "Kira":
                        String kiraSonuc = reader.readLine();
                        kira.setText("Kira : " + kiraSonuc);
                        break;

                    case "Elektrik":
                        String elektrikSonuc = reader.readLine();
                        elektrik.setText("Elektrik : " + elektrikSonuc);
                        break;

                    case "Su Gideri":
                        String suSonuc = reader.readLine();
                        su.setText("Su : " + suSonuc);
                        break;

                    case "Personel Gideri":
                        String personelSonuc = reader.readLine();
                        personel.setText("Personel : " + personelSonuc);
                        break;

                    case "Toplam":
                        String toplamSonuc = reader.readLine();
                        toplam.setText("Toplam : " + toplamSonuc);
                        break;
                        
                    case "":
                        devam = 0;

                    default:
                        devam = 0;
                        break;

                }
            }

        } catch (FileNotFoundException ex) {

        } catch (IOException ex) {
            Logger.getLogger(giderGor.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_onayBtnActionPerformed

